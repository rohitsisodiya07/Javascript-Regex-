// 34. Which array methods are mutating and non-mutating?

// Mutating array methods (change original array)

// These methods modify the original array:

// push() → add at end

// pop() → remove from end

// shift() → remove from start

// unshift() → add at start

// splice() → add / remove / replace

// sort() → sort array

// reverse() → reverse array

// fill() → fill values

// copyWithin() → copy part inside array

//  Non-mutating array methods (do NOT change original array)

// These methods return a new array/value and keep original safe:

// map()

// filter()

// reduce()

// slice()

// concat()

// flat()

// flatMap()

// includes()

// indexOf(), lastIndexOf()

// find(), findIndex()

// some(), every()

// join()

// Modern non-mutating (ES2023)

// toSorted()

// toReversed() 

// toSpliced()
